$dir = 'lang/tr';
$files = glob($dir . '/*.php');
foreach($files as $f) {
    $c = file_get_contents($f);
    
    // Find all words containing replacement char
    if (preg_match_all('/[^\s\'"<>={}()\[\],.;:!?-]*\xEF\xBF\xBD[^\s\'"<>={}()\[\],.;:!?-]*/u', $c, $m)) {
        foreach($m[0] as $w) {
            $clean = str_replace("\xEF\xBF\xBD", "", $w);
            if (isset($replacements[$clean])) {
                $c = str_replace($w, $replacements[$clean], $c);
            } else {
                echo "Missing replacement for: $clean (original: $w) in $f\n";
            }
        }
    }
    file_put_contents($f, $c);
}
// Fix com/p/... manually
